package org.tutorial;

public class PrimitiveDemo
{
	public static void main(String[] args)
	{
		byte b = 100;
		short s = 125;
		int n1 = 123456;
		int n2 = -9874;
		
		long amountValue = 1234567892;
		float intRate = 17.77f;
		double cosVal = 1233.23d;
		boolean flag = true;
		char ch1 = 'B';
		char ch2 = 65; // code for A
		
		System.out.println(" byte value    : " + b);
		System.out.println(" short value   : " + s);
		System.out.println(" int value     : " + n1);
		System.out.println(" int value     : " + n2);
		System.out.println(" long value    : " + amountValue);
		System.out.println(" float value   : " + intRate);
		System.out.println(" double value  : " + cosVal);
		System.out.println(" boolean value : " + flag);
		System.out.println(" char value    : " + ch1);
		System.out.println(" char value    : " + ch2);
		
	}
}
