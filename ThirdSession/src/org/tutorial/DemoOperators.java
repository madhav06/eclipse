package org.tutorial;

public class DemoOperators 
{
	public static void main(String args[]) 
	{
		int value = 2 + 3;
		System.out.println(value); // value is now 5
		
		value = value - 1;
		System.out.println(value); // value is now 4
		
		value = value/2;
		System.out.println(value); // value is now 2
		
		value = value*2;
		System.out.println(value); // value is now 4
		
		value = value + 7;
		value = value%7;
		System.out.println(value); // value is now 4
		
	}
}
